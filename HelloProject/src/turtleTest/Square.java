package turtleTest;
import ch.aplu.turtle.*;

import java.awt.Color;


public class Square {
    private double xcoord;
    private double ycoord;
    private double size;
    Turtle point;
    public Square(double x, double y, double s)
    {
        xcoord = x;
        ycoord = y;
        this.size = s;
        point = new Turtle();
        point.setColor(Color.green);

        //Turtle - .setColor(Color.color)
        //         .penUp()
        //         .moveTo(x,y)
        //         .penDown()
        //         .forward(size)
        //         .right(radian)
    }
    public void draw()
    {
        point.penUp();
        point.moveTo(xcoord,ycoord);
        point.penDown();
        for(int i=0;i<4;i++)
        {
            point.forward(size);
            point.right(90);
        }
    }
    public static void main(String[] args)
    {
        Square s = new Square(0.0,100.0,100.0);
        s.draw();
    }

}
